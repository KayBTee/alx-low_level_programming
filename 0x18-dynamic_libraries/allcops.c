/**
 * add - adds two numbers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - divides two numbers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - modulus of two numbers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
        return (a % b);
}
